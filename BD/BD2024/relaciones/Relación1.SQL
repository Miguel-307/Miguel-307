SELECT ALUMNO.*
FROM ALUMNO, MATRICULA
WHERE ALUMNO.AL# = MATRICULA.AL#
AND MATRICULA.ASIG# = 'BD3';

-- 2
SELECT ALUMNO.*
FROM ALUMNO, MATRICULA, ASIGNATURA
WHERE ALUMNO.AL# = MATRICULA.AL#
AND MATRICULA.ASIG# = ASIGNATURA.ASIG#
AND ASIGNATURA.NOMBRE = 'Bases de Datos';

-- 3
SELECT PROFESOR.*
FROM PROFESOR, ASIGNATURA
WHERE PROFESOR.ASIG# = ASIGNATURA.ASIG#
AND ASIGNATURA.NOMBRE = 'Programación I';

-- 4
SELECT ASIGNATURA.NOMBRE, ASIGNATURA.CURSO
FROM ASIGNATURA, MATRICULA
WHERE ASIGNATURA.ASIG# = MATRICULA.ASIG#
AND MATRICULA.AL# = (SELECT AL# FROM ALUMNO WHERE NOMBRE = 'José Pérez');

